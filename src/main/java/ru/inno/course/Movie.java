package ru.inno.course;

public class Movie {
    String title;
    int rating;
    String genre;
    String country;
    boolean isOscar;


    public Movie(String title, int rating, String genre, String country, boolean isOscar) {
        this.title = title;
        this.rating = rating;
        this.genre = genre;
        this.country = country;
        this.isOscar = isOscar;


    }
}
